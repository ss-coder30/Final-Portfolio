// smooth scroll

document.addEventListener('DOMContentLoaded', function() {
  // Get the button element
  var downloadButton = document.getElementById('downloadButton');

  // Add a click event listener to the button
  downloadButton.addEventListener('click', function() {
    // Replace 'path/to/your/pdf.pdf' with the actual path to your PDF file
    var pdfPath = __dirname + '/Resume-Shaurya-Srivastava (1).pdf';

    // Create a temporary anchor element
    var tempLink = document.createElement('a');

    // Set the link's href attribute to the PDF path
    tempLink.href = pdfPath;

    // Set the download attribute to specify the suggested file name
    tempLink.setAttribute('download', 'Resume-Shaurya-Srivastava (1).pdf');

    // Append the link to the body of the document
    document.body.appendChild(tempLink);
    // Programmatically click the link to trigger the download
    tempLink.click();

    // Remove the temporary link from the DOM
    document.body.removeChild(tempLink);
  });
});

$(document).ready(function(){
    $(".navbar .nav-link").on('click', function(event) {

        if (this.hash !== "") {

            event.preventDefault();

            var hash = this.hash;

            $('html, body').animate({
                scrollTop: $(hash).offset().top
            }, 700, function(){
                window.location.hash = hash;
            });
        } 
    });
});

// protfolio filters
$(window).on("load", function() {
    var t = $(".portfolio-container");
    t.isotope({
        filter: ".new",
        animationOptions: {
            duration: 750,
            easing: "linear",
            queue: !1
        }
    }), $(".filters a").click(function() {
        $(".filters .active").removeClass("active"), $(this).addClass("active");
        var i = $(this).attr("data-filter");
        return t.isotope({
            filter: i,
            animationOptions: {
                duration: 750,
                easing: "linear",
                queue: !1
            }
        }), !1
    });
});


